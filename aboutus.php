  <strong><font size="+3">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Know About Virtual University</font> </strong>
<p>&nbsp;&nbsp;&nbsp;&nbsp;1. A Virtual University does not have lecture halls, auditoriums, class rooms, labs or a campus.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;2. A Virtual University is run by a management office.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;3. The main method of academic achievement in the University is through Distant Education through all available methods.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; e. g. correspondence, online courses, video, self learning, e-learning, Contact Classes and evaluation of work experience.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;4. Virtual Universities rely on methodical research methods, publication or academic and educational consultancy for each member in the field of study.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;5. The Headquarters of a Virtual University does not necessarily exist in the same country where the license is issued.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;6. Virtual Universities utilize and are supported by IT and Logistics firms as well as Academic Consultants in their education, financial and operational facilities to better serve members.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;7. Virtual Universities are subject to verification and authentication by academic and Scientific private non-governmental organizations.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;8. Virtual Universities are represented by accredited agencies or offices in various countries, not by individuals.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;9. Virtual Universities are owned by private institutions or corporations or Non-Governmental Organisations with interest in academic investment and educational goals.</p>
<p><strong><font size="+2">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Advantages of Virtual University</font></strong></p>
<p></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;The virtualization process of universities has been offering so many advantages over the traditional university setup. Some of the advantages are:</p>
<p></p>
<p>&nbsp;&nbsp;&nbsp;&nbsp; > It is advantageous for those who can not pursue the regular course due to some compulsion like in service, financial in-competency.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp; > Educational opportunities are close to home</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp; > Students receive exposure to telecommunication technologies.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp; > Access to media-rich learning environment.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp; > Opportunities to develop technology competencies.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp; > Contact with students in other locations in the region</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp; > Access to global resources and experts</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp; > Learning-on-demand</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp; > Anytime, Anywhere, Anything at learners’ choice and ease.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp; > Customization of content</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp; > Interactivity in content</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp; > Self paced learning with no time limit</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp; > Cost effective</p>
