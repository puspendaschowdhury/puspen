<p class="style4 style2 style3">MCA is a Post graduate degree course which is of 3 year long duration. MCA course is offered to students in most of the Universities and colleges in India. The students who want to pursue a career in the software field can take up MCA course after a graduation. The MCA course offers excellent job opportunity for talented students in software firms and also in Multinational companies with excellent salary.</p>
<p align="center" class="style5">Eligibility for MCA Course:</p>
<span class="style8">The students who wish to join an MCA course is required to have a Bachelors degree in Computer science, information Science, IT etc from a recognized university. The candidates who pose a PGDCA from a reputed university or its equivalent course are also eligible for MCA.

The students who are applying for MCA must also have studied maths as a main subject in 10+2 level are only eligible. But some university offers students who haven’t studied math’s to join the course if they are willing to do an additional subject in the 1st semester of MCA. </span>
