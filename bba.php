  <p class="style4 style2 style3">The students are prepared to discover changes created in management career. Business Administration study methodology includes training through practical classes. The program includes case studies, industrial visits etc. BBA course also covers projects and presentations. Bachelor’s degree courses in Business Administration/ Business Studies/ Management Studies are called by various names. These programs have different names like BBA, BBS and BMS. </p>
<p align="center" class="style5">Eligibility for MCA Course:</p>
<span class="style8">Usually there are no entrance tests for getting admission into this program. Some institutes select only Indian citizens. The applicants must have passed the HSC (10+2) from a reputed education board. The admission is mostly carried out on merit basis. The marks needed in the +2 are at least 55%. Some institutes are keeping minimum age limit as 17 and a maximum of 25 years.
